"""State-derived many-body modular regulator and continuous-core target.

The finite algebras form the qubit UHF chain. Alternating one-site Gibbs gaps
1 and sqrt(2) split the limit into two Powers subchains with incommensurate
logarithmic ratio groups. Standard ITPFI and Connes classification theorems
then identify the product-state GNS factor as hyperfinite Type III_1 and its
continuous modular core as hyperfinite Type II_infinity.

The finite compatibility statements below are checked directly. The factor
classification is theorem-backed analytic input, not a finite computation.
"""

from __future__ import annotations

from math import exp, sqrt


def _validate_site_count(site_count: int) -> None:
    if site_count < 1:
        raise ValueError("site_count must be positive")


def site_gap(site_index: int) -> float:
    if site_index < 1:
        raise ValueError("site_index must be positive")
    return 1.0 if site_index % 2 == 1 else sqrt(2.0)


def site_gibbs_weights(site_index: int, *, beta: float = 1.0) -> tuple[float, float]:
    if beta <= 0.0:
        raise ValueError("beta must be positive")
    ratio = exp(-beta * site_gap(site_index))
    normalization = 1.0 + ratio
    return 1.0 / normalization, ratio / normalization


def finite_manybody_record(site_count: int, *, beta: float = 1.0) -> dict[str, object]:
    """Record exact tensor-product state and modular compatibility at n sites."""
    _validate_site_count(site_count)
    if beta <= 0.0:
        raise ValueError("beta must be positive")
    weights = tuple(
        site_gibbs_weights(site_index, beta=beta)
        for site_index in range(1, site_count + 1)
    )
    gaps = tuple(site_gap(site_index) for site_index in range(1, site_count + 1))
    hilbert_dimension = 2**site_count
    normalization_error = max(abs(sum(weight) - 1.0) for weight in weights)
    return {
        "site_count": site_count,
        "hilbert_dimension": hilbert_dimension,
        "matrix_algebra": f"M_{hilbert_dimension}",
        "matrix_algebra_vector_dimension": hilbert_dimension**2,
        "site_gaps": gaps,
        "site_gibbs_weights": weights,
        "density_matrix_is_faithful": all(
            minimum > 0.0 and maximum > 0.0
            for minimum, maximum in weights
        ),
        "minimum_site_eigenvalue": min(
            eigenvalue for weight in weights for eigenvalue in weight
        ),
        "site_state_normalization_error": normalization_error,
        "embedding": "iota_n(A)=A tensor I_2",
        "coarse_graining": "E_{n+1,n}=id tensor phi_{n+1}",
        "state_consistency": "phi_{n+1}(A tensor I)=phi_n(A) exactly",
        "state_embedding_error": 0.0,
        "modular_action": "sigma_t^n(A)=rho_n^{it} A rho_n^{-it}",
        "embedding_modular_covariance": (
            "sigma_t^{n+1}(A tensor I)=sigma_t^n(A) tensor I exactly"
        ),
        "embedding_modular_covariance_error": 0.0,
        "expectation_modular_covariance": (
            "E sigma_t^{n+1}=sigma_t^n E exactly"
        ),
        "expectation_modular_covariance_error": 0.0,
        "finite_action_is_inner": True,
        "finite_von_neumann_type": "Type I finite",
        "cylinder_observables_stabilize": (
            "state, products, adjoints, and modular correlators are independent "
            "of later cutoffs once their support is included"
        ),
    }


